function shuffleArray(array: []) {
   for (let i = array.length - 1; i > 0; i--) {
      const j = Math.floor(Math.random() * (i + 1));
      [array[i], array[j]] = [array[j], array[i]]; // Swap elements
   }
}

// Shuffle and pick drugs without repeating
export function selectRandomDrugs() {
   if (sampleDrugs.length === 0) {
      return [];
   }
   // Clone the array to avoid modifying the original dataset
   const shuffledDrugs: any = [...sampleDrugs];
   shuffleArray(shuffledDrugs);
   // Select the first 'num' drugs from the shuffled list
   return shuffledDrugs[0];
}